    static double[] sampleStats(int[] count) {
        long max = 0, min = 256, mode = 0, modeCount = 0, c = 0, sum = 0;
        for (int i = 0; i < count.length; i++) {
            if (count[i] > 0) {
                max = i;
                if (min == 256) min = i;
                sum += (long) count[i] * i;
                c += count[i];
                if (modeCount < count[i]) {
                    modeCount = count[i];
                    mode = i;
                }
            }
        }
        double mean = (1.0 * sum) / c, median = 0;

        long cc = 0, prev = 0, cHalf = c % 2 == 1 ? c / 2 + 1 : c / 2;
        if (c % 2 == 0) {
            for (int i = 0; i < count.length; i++) {
                if (count[i] > 0) {
                    if (cc == cHalf) {
                        median = (i + prev) / 2.0;
                        break;
                    }
                    if (cc + count[i] > cHalf) {
                        median = i;
                        break;
                    }
                    cc += count[i];
                    prev = i;
                }
            }
        } else {
            for (int i = 0; i < count.length; i++) {
                if (count[i] > 0) {
                    if (cc + count[i] >= cHalf) {
                        median = i;
                        break;
                    }
                    cc += count[i];
                }
            }
        }

        return new double[]{min, max, mean, median, mode};
    }
